SELECT
	S.NOME AS SELECAO,
	CLA.VITORIAS,
	CLA.DERROTAS,
	CLA.EMPATES,
	CLA.PONTOS
FROM (
	SELECT
		C.ID_SELECAO,
		C.VITORIAS,
		C.DERROTAS,
		C.EMPATES,
		C.PONTOS
	FROM
		CLASSIFICACAO C
	WHERE 
		C.ID_RODADA = 3
	) AS CLA
INNER JOIN
	SELECAO AS S ON S.ID_SELECAO = CLA.ID_SELECAO
ORDER BY CLA.PONTOS DESC