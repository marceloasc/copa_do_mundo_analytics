CREATE TABLE JOGADOR (
	ID_JOGADOR INT PRIMARY KEY NOT NULL,
	NOME VARCHAR(50) NOT NULL,
	POSICAO VARCHAR(50) NOT NULL,
	ID_SELECAO INT NOT NULL
);

CREATE TABLE SELECAO (
	ID_SELECAO INT PRIMARY KEY NOT NULL,
	NOME VARCHAR(50) NOT NULL,
	ID_GRUPO INT NOT NULL
);

CREATE TABLE GRUPO (
	ID_GRUPO INT PRIMARY KEY NOT NULL,
	LETRA VARCHAR(1) NOT NULL
);

CREATE TABLE PARTIDA (
	ID_PARTIDA INT PRIMARY KEY NOT NULL,
	DATA_PARTIDA DATE NOT NULL,
	HORARIO TIME NOT NULL,
	ID_ESTADIO INT NOT NULL,
	ID_GRUPO INT NOT NULL,
	ID_SELECAO_CASA INT NOT NULL,
	GOL_CASA INT NOT NULL,
	ID_SELECAO_FORA INT NOT NULL,
	GOL_FORA INT NOT NULL
);

CREATE TABLE LOCAL (
	ID_ESTADIO INT PRIMARY KEY NOT NULL,
